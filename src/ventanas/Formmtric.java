    private void jBAgregar4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAgregar4ActionPerformed

        aMatriculas dts = new aMatriculas();
        Fmatriculas func = new Fmatriculas();
        func.Mostrar(dts);

        //se valida que se llenen los datos        
        if (jCAnioMtr.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(rootPane, " debe ingresar el aÃ±o de la matricula");
            jCAnioMtr.requestFocus();
            return;
        }

        if (jCSemestre.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(rootPane, " debe ingresar el semestre de matricula");
            jCSemestre.requestFocus();
            return;
        }

        if (jCJornada.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(rootPane, " debe ingresar la jornada de matricula");
            jCJornada.requestFocus();
            return;
        }

        if (jCCiclo.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(rootPane, " debe ingresar el ciclo de la matricula");
            jCCiclo.requestFocus();
            return;
        }

        // capturar los datos 
        dts.setIdEstudiante(Integer.parseInt(Formestu.idestudiante));
        dts.setAniomatr(jCAnioMtr.getSelectedItem().toString().trim());
        dts.setSemestre(jCSemestre.getSelectedItem().toString().trim());
        dts.setJornada(jCJornada.getSelectedItem().toString().trim());
        dts.setCiclo(jCCiclo.getSelectedItem().toString().trim());
        dts.setIdmatricula(IdMatricula);

        if (func.insertar(dts)) {
            JOptionPane.showMessageDialog(null, "matricula creado con exito");

            Deshabilitar();
            jBNext.setEnabled(true);
            jCJornada.requestFocus();
        }

    }//GEN-LAST:event_jBAgregar4ActionPerformed

    private void jBNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBNextActionPerformed

        Formnotas form = new Formnotas();
        Escritorio.add(form);
        Dimension desktopSize = Escritorio.getSize();
        Dimension FrameSize = form.getSize();
        form.setLocation((desktopSize.width - FrameSize.width) / 2, (desktopSize.height - FrameSize.height) / 2);
        form.show();
        this.setVisible(false);

        aMatriculas dts = new aMatriculas();
        Fmatriculas func = new Fmatriculas();
        func.Mostrar(dts);
        Formnotas.jLSemestre.setText("SEMESTRE: " + jCSemestre.getSelectedItem().toString());
        Formnotas.jLAnio.setText("AÃ‘O: " + jCAnioMtr.getSelectedItem().toString());
        Formnotas.jLJornada.setText("JORNADA: " + jCJornada.getSelectedItem().toString());
        Formnotas.jLCiclo.setText("CICLO: " + jCCiclo.getSelectedItem().toString());
        String ciclo = Integer.toString(jCCiclo.getSelectedIndex() + 1);
        Formnotas.JTciclo.setText(ciclo);
        anio = jCAnioMtr.getSelectedItem().toString();
        semes = jCSemestre.getSelectedItem().toString();
        jornada = jCJornada.getSelectedItem().toString();
        cicl = jCCiclo.getSelectedItem().toString();
        IdMatricula = dts.getIdmatricula();

        if (cicl.equals("Basico")) {
            Formnotas.jLaMat1.setText("Matematicas");
            Formnotas.jLaMat2.setText("Ciencias Sociales");
            Formnotas.jLaMat3.setText("Ciencias Naturales");
            Formnotas.jLaMat4.setText("EspaÃ±ol");
            Formnotas.jLaMat5.setText("Ingles");
            Formnotas.jLaMat6.setText("Artes");

        } else {
            Formnotas.jLaMat1.setText("Matematicas II");
            Formnotas.jLaMat2.setText("Ciencias Sociales II");
            Formnotas.jLaMat3.setText("Ciencias Naturales II");
            Formnotas.jLaMat4.setText("EspaÃ±ol II");
            Formnotas.jLaMat5.setText("Ingles II");
            Formnotas.jLaMat6.setText("Desarrollo Personal");

        }

    }//GEN-LAST:event_jBNextActionPerformed

