    public void validar() {
        jBNext.setEnabled(false);
        if (jCAnioMtr.getSelectedIndex() != -1
                && jCCiclo.getSelectedIndex() != -1
                && jCJornada.getSelectedIndex() != -1
                && jCSemestre.getSelectedIndex() != -1) {
            jBAgregar4.setEnabled(false);
            jBNext.setEnabled(true);
            jCJornada.setEnabled(false);
            jCCiclo.setEnabled(false);
        } else if (jCAnioMtr.getSelectedIndex() != -1
                && jCSemestre.getSelectedIndex() != -1) {
            //jBAgregar4.setEnabled(true);
            jCJornada.setEnabled(true);
            jCCiclo.setEnabled(true);
        }

    }

    public void check() {

        aMatriculas dts = new aMatriculas();
        Fmatriculas func = new Fmatriculas();
        jBAgregar4.setEnabled(false);

        anio = jCAnioMtr.getSelectedItem().toString();
        switch (jCSemestre.getSelectedIndex()) {
            case 0:
                semes = "1";
                break;
            case 1:
                semes = "2";
                break;
            default:
                semes = "null";
                break;
        }
        func.consultamtr(dts, anio, semes);
        jCJornada.setSelectedItem(dts.getJornada());
        jCCiclo.setSelectedItem(dts.getCiclo());

        if (dts.getCiclo() != (null)) {
            jBAgregar4.setEnabled(true);
            jCJornada.setSelectedItem(dts.getJornada());
            jCCiclo.setSelectedItem(dts.getCiclo());
        }
        validar();

    }

